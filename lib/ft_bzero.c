#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*save_str;

	i = 0;
	save_str = s;
	while (i < n)
	{
		*save_str = '\0';
		i = i + 1;
		save_str = save_str + 1;
	}
}
